public class Emp {

    private int kod;
    private String nev;
    private String szulido;
    private String lakohely;
    private int iq;

    public int getKod() {
        return kod;
    }

    public String getNev() {
        return nev;
    }

    public String getSzulido() {
        return szulido;
    }

    public String getLakohely() {
        return lakohely;
    }

    public int getIq() {
        return iq;
    }


    public Emp(int kod, String nev, String szulido, String lakohely, int iq) {
        this.kod = kod;
        this.nev = nev;
        this.szulido = szulido;
        this.lakohely = lakohely;
        this.iq = iq;
    }





}
